    private String tinhThoiGianLamViec(
        java.time.LocalDateTime thoiGianBatDau,
        java.time.LocalDateTime thoiGianKetThuc
) {
    if (thoiGianBatDau == null || thoiGianKetThuc == null) {
        return "";
    }

    java.time.Duration duration =
            java.time.Duration.between(thoiGianBatDau, thoiGianKetThuc);

    long tongPhut = duration.toMinutes();

    if (tongPhut < 0) {
        return "";
    }

    long gio = tongPhut / 60;
    long phut = tongPhut % 60;

    if (gio > 0 && phut > 0) {
        return gio + " giờ " + phut + " phút";
    }

    if (gio > 0) {
        return gio + " giờ";
    }

    return phut + " phút";
}
