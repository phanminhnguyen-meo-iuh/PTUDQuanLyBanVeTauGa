private int laySoTo(javax.swing.JTextField txt) {
    String s = txt.getText().trim();

    if (s.isEmpty()) {
        return 0;
    }

    try {
        return Integer.parseInt(s);
    } catch (NumberFormatException e) {
        return 0;
    }
}

private double layTienChuyenKhoan() {
    String s = jTextField7.getText().trim();

    if (s.isEmpty()) {
        return 0;
    }

    s = s.replace(".", "").replace(",", "");

    try {
        return Double.parseDouble(s);
    } catch (NumberFormatException e) {
        return 0;
    }
}

